package com.kt.largesreen.player.data;

public class TextInfo {
	private int x;
	private int y;
	private int width;
	private int height;
	private String[] text;
	private int size;
	private String color;
	private String style;
	private String background;
	private String anim;
	private int frequence;
	private boolean singleline;
	private int interval;
	private boolean startScroll;
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}
	/**
	 * @param x the x to set
	 */
	public void setX(int x) {
		this.x = x;
	}
	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
	/**
	 * @param y the y to set
	 */
	public void setY(int y) {
		this.y = y;
	}
	/**
	 * @return the width
	 */
	public int getWidth() {
		return width;
	}
	/**
	 * @param width the width to set
	 */
	public void setWidth(int width) {
		this.width = width;
	}
	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}
	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}
	/**
	 * @return the text
	 */
	public String[] getText() {
		return text;
	}
	/**
	 * @param text the text to set
	 */
	public void setText(String[] text) {
		this.text = text;
	}
	/**
	 * @return the size
	 */
	public int getSize() {
		return size;
	}
	/**
	 * @param size the size to set
	 */
	public void setSize(int size) {
		this.size = size;
	}
	/**
	 * @return the color
	 */
	public String getColor() {
		return color;
	}
	/**
	 * @param color the color to set
	 */
	public void setColor(String color) {
		this.color = color;
	}
	/**
	 * @return the style
	 */
	public String getStyle() {
		return style;
	}
	/**
	 * @param style the style to set
	 */
	public void setStyle(String style) {
		this.style = style;
	}
	/**
	 * @return the background
	 */
	public String getBackground() {
		return background;
	}
	/**
	 * @param background the background to set
	 */
	public void setBackground(String background) {
		this.background = background;
	}
	/**
	 * @return the anim
	 */
	public String getAnim() {
		return anim;
	}
	/**
	 * @param anim the anim to set
	 */
	public void setAnim(String anim) {
		this.anim = anim;
	}
	/**
	 * @return the frequence
	 */
	public int getFrequence() {
		return frequence;
	}
	/**
	 * @param frequence the frequence to set
	 */
	public void setFrequence(int frequence) {
		this.frequence = frequence;
	}
	/**
	 * @return the singleline
	 */
	public boolean getSingleline() {
		return singleline;
	}
	/**
	 * @param singleline the singleline to set
	 */
	public void setSingleline(boolean singleline) {
		this.singleline = singleline;
	}
	/**
	 * @return the interval
	 */
	public int getInterval() {
		return interval;
	}
	/**
	 * @param interval the interval to set
	 */
	public void setInterval(int interval) {
		this.interval = interval;
	}
	public boolean getStartScroll() {
		return startScroll;
	}
	public void setStartScroll(boolean startScroll) {
		this.startScroll = startScroll;
	}
}